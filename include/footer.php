                <h3>Technische Voraussetzungen</h3>
                <h4>Anforderungen an den Browser</h4>
                <p>Nachfolgend sind diejenigen Browser aufgelistet, mit denen die MedienDB verwendet werden kann:</p>
                <ul>
                	<li>MS Internet Explorer ab Version 10.0</li>
                	<li>Die aktuellste Version von MS Edge</li>
                	<li>Die aktuellste Version von Firefox</li>
                	<li>Die aktuellste Version von Chrome</li>
                	<li>Die aktuellste Version von Safari</li>
                </ul>
                <h4>Browser-Einstellungen</h4>
                <p>Damit die MedienDB korrekt und zuverlässig funktioniert, muss Ihr Browser die 128 Bit Daten-Verschlüsselung, JavaScript und Cookies unterstützen. Falls die Anwendung mit den Browsern "Internet Explorer", "Firefox" oder in Android mit dem Browser "Internet" nicht funktioniert, sollten Sie folgende Einstellung im Menü des Browsers kontrollieren resp. anpassen:</p>
                <h5>Datenverschlüsselung, SSL 3.0</h5>
                <ul>
                	<li>Firefox | Menü: "Extras / Einstellungen / Erweitert / Verschlüsselung" unter dem Punkt "Protokolle" den Eintrag "SSL 3.0 verwenden" aktivieren (Ab der Version 23 von Firefox ist die SSL 3.0 Verschlüsselung automatisch aktiviert.)</li>
                	<li>Internet Explorer | Menü: "Extras / Internetoptionen / Erweitert / Standard bzw. erweiterte Einstellungen wiederherstellen" oder unter dem Punkt "Sicherheit" den Eintrag "SSL 3.0 verwenden" aktivieren.</li>
                </ul>
                <h5>Aktivieren von JavaScript</h5>
                <ul>
                	<li>Firefox | Menü: "Extras / Einstellungen / Inhalt" den Eintrag "JavaScript aktivieren" selektieren (Ab der Version 23 von Firefox ist JavaScript automatisch aktiviert.)</li>
                	<li>Internet Explorer | Menü: "Extras / Internetoptionen / Sicherheit / Internet / Sicherheitsstufe für diese Zone" / Regler auf "Mittel bis hoch" setzen</li>
                </ul>
                <h5>Aktivieren von Cookies</h5>
                <ul>
                	<li>Firefox | Menü: "Extras / Einstellungen / Datenschutz" den Eintrag "Cookies akzeptieren" selektieren</li>
                	<li>Menü: "Extras / Internetoptionen / Datenschutz / Erweitert / Automatische Cookiebehandlung aufheben" deaktivieren</li>
                </ul>
